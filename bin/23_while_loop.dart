void main() {
  /**
   * while loop adalah versi perulangan sama dengan for namun lebih sederhana
   * di while loop hanya ada kondisi, tanpa ada init statement dan juga post statement
   */

  // kode while loop
  int counter = 1;
  while (counter <= 10) {
    print('perulanganke- $counter');
    counter++;
  }
}
