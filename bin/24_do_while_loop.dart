void main() {
  /**
   * DO WHILE LOOP
   * do while loop adalah perulangan yang mirip dengan while loop 
   * perbedaan hanya pada pengecekan kondisi
   * pengecekan kondisi di di while loop di lakukan di awal sebelum perulangan 
   * sedangakan pada so while pengecekan kondi si di akhir setelah perulangan di lakukan
   * jadi perulangan minimal akan satu kali di jalankan, walaupun kondisi bernilai false
   */

  // code do wahile loop
  var counter = 1;
  do {
    print('perulangan ke- $counter');
    counter++;
  } while (false);
}
