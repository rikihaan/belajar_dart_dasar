void main() {
  /**
   * Number adalah tipe data angka, terdapat dua buah jenis type data number yaitu , int dan double
   * int adalah bilangan bulat
   * double adalah tipe bilangan desimal
   * penulisan koma dalam double menggunkan titik buka menggunakan koma jika kita akan membuat bilangan desimal 
   * bilangan desimal 0,5 (nol koma lima) maka di tulis 0.5
   */

  int number1 = 205;
  double number2 = 10.5;

  print(number1);
  print(number2);

  // jika kita ingin bisa menggunakan type number integer dan double kita bisa gunakan tipe data num, atau tipe kata kunci (num) bisa menampung bilangan int atau bilaianga desimal(double)

  // type data NUM
  num number3 = 10;
  num number4 = 20.7;

  print(number3);
  print(number4);
}
