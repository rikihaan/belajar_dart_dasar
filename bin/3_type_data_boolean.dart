void main() {
  /***
   * Boolean adalah tipe data yang hanya memiliki dua nilai, yaitu benar dan salah
   * Boolean direpresentasikan dengan kata kunci bool
   * untuk nilai benar, menggunakan kata kunci true
   * untuk salah, menggunkan kata kunci false
   */

  // kode type data boolean
  bool finish = false;
  print(finish);

  finish = true;
  print(true);
}
