void main() {
  // petambahan +
  var result1 = 20 + 20;

  // pengurangan -
  var result2 = 100 - 90;

  // negative
  var result3 = -5;

//  positive
  var result4 = 5;

// perkalian
  var result5 = 5 * 5;

// pembagian dengan hasil bilagan double atau desimal
  var result6 = 10 / 3;

// pembagian dengan hasil int atau bilangan bulat
  var result7 = 10 ~/ 3;

  // sisa bagi %
  var result8 = 10 % 3;

  print(result1);
  print(result2);
  print(result3);
  print(result4);
  print(result5);
  print(result6);
  print(result7);
  print(result8);
}
