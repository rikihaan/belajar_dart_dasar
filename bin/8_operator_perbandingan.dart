void main() {
  /**
   * 
   * Operator perbandingan, adalah oerator yang hasilnya,data boolean
   * operator perbandingan bisa digunakan di tipe data string dan number
   * 
   */

  // sama dengna ==
  var a = 1;
  var b = 1;
  var hasil1 = a == b;
  print(hasil1);

  // tidak sama dengan
  var c = 4;
  var hasil2 = a != c;
  print(hasil2);

  // lebih dari
  var x = 10;
  var y = 30;
  var hasil3 = y > x;
  print(hasil3);

  // kurang dasri
  var hasil4 = y < x;
  print(hasil4);

  // lebih dari sama dengan
  var r = 90;
  var s = 100;
  var hasil5 = r >= s;
  print(hasil5);

  // kurang dari sama dengan
  var hasil6 = r <= s;
  print(hasil6);
}
